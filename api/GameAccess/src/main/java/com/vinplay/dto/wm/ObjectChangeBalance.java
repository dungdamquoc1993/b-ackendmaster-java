package com.vinplay.dto.wm;

public class ObjectChangeBalance {

	private String msg;
	private String yourOrderNum;
	private String orderId;
	private String cash;

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

	public String getYourOrderNum() {
		return yourOrderNum;
	}

	public void setYourOrderNum(String yourOrderNum) {
		this.yourOrderNum = yourOrderNum;
	}

	public String getOrderId() {
		return orderId;
	}

	public void setOrderId(String orderId) {
		this.orderId = orderId;
	}

	public String getCash() {
		return cash;
	}

	public void setCash(String cash) {
		this.cash = cash;
	}

}
